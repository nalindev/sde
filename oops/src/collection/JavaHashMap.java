package collection;

import java.util.*;

public class JavaHashMap {

	/*
	 * Java Hashmap 
	 *    - it implements Map interface
	 *    - it is non syncronized
	 *    - it maintains no order
	 *      
	 */
	public static void main(String[] args) {
		HashMap<Integer, String> map = new HashMap<>();
		
		map.put(0, "nalin");
		map.put(1, "saurav");
		map.put(4, "nishu");
		
		
		
		
	}

}
